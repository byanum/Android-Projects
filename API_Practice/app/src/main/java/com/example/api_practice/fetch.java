package com.example.api_practice;

public class fetch {
    private String data;
    private String something;

    public fetch() {
        this.data = data;
        this.something = something;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public String getSomething() {
        return something;
    }

    public void setSomething(String something) {
        this.something = something;
    }
}
